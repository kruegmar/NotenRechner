    private String roundValue(float input){
        BigDecimal bd = new BigDecimal(input);
        return bd.round(new MathContext(3)).toString();
    }

    private String computeEndMark(){
        float mean = 
                Float.parseFloat(this.jLabel_GdI_Val.getText().toString())*6
                + Float.parseFloat(this.jLabel_GdM_Val.getText().toString())*8
                + Float.parseFloat(this.jLabel_Prog_Val.getText().toString())*8
                + Float.parseFloat(this.jLabel_BsN_Val.getText().toString())*8
                + Float.parseFloat(this.jLabel_PI_Val.getText().toString())*10
                + Float.parseFloat(this.jLabel_CuM_Val.getText().toString())*14
                + Float.parseFloat(this.jLabel_Mat_Val.getText().toString())*6
                + Float.parseFloat(this.jLabel_BWL_Val.getText().toString())*10
                + Float.parseFloat(this.jLabel_WP_Val.getText().toString())*30;
        mean /= 100;

        mean = ((mean*7) + (Float.parseFloat(this.jLabel_BA.getText().toString())*3))/10;

        return roundValue(mean);
    }

    private void refreshEndMark(){
        this.jLabel_EndMark.setText(this.computeEndMark());
    }

